// Storage key for recently used symbols
export const STORAGE_KEY = 'recently-used-symbols'
export const MAX_RECENT = 30

// Function to get all symbols for search
export function getAllSymbols(): string[] {
	const allSymbols: string[] = []
	Object.values(symbolCategories).forEach(category => {
		allSymbols.push(...category.symbols)
	})
	return Array.from(new Set(allSymbols)) // Remove duplicates
}

// Function to search symbols
export function searchSymbols(query: string): string[] {
	if (!query) return []

	const searchTerm = query.toLowerCase()
	const results: string[] = []

	Object.entries(symbolCategories).forEach(([key, category]) => {
		// Search in category name
		if (category.name.toLowerCase().includes(searchTerm)) {
			results.push(...category.symbols)
		}

		// Search in category key
		if (key.toLowerCase().includes(searchTerm)) {
			results.push(...category.symbols)
		}
	})

	// Also search for symbols that contain the query (for unicode names)
	const allSymbols = getAllSymbols()
	allSymbols.forEach(symbol => {
		if (symbol.includes(query) && !results.includes(symbol)) {
			results.push(symbol)
		}
	})

	return Array.from(new Set(results))
}
